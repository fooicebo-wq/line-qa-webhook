// ── LINE API 回覆 ─────────────────────────────────────

async function replyToLine(replyToken, messages) {
  await fetch('https://api.line.me/v2/bot/message/reply', {
    method: 'POST',
    headers: {
      'Content-Type': 'application/json',
      'Authorization': `Bearer ${LINE_TOKEN}`
    },
    body: JSON.stringify({ replyToken, messages })
  });
}

// ── 主處理函式 ────────────────────────────────────────

export default async function handler(req, res) {
  if (req.method !== 'POST') {
    return res.status(200).json({ status: 'ok' });
  }

  try {
    const events = req.body?.events || [];

    for (const event of events) {
      if (!event.replyToken || !LINE_TOKEN) continue;

      // ── 加入好友：自動推送卡片選單 ──
      // （歡迎文字由 LINE 內建「加入好友的歡迎訊息」負責，此處只補卡片避免重複）
      if (event.type === 'follow') {
        await replyToLine(event.replyToken, [buildFlexMenu()]);
        continue;
      }

      if (event.type !== 'message' || event.message?.type !== 'text') continue;

      const userMsg = event.message.text.trim();
      const replyToken = event.replyToken;

      // 跳過原生精確關鍵字
      if (NATIVE_EXACT.has(userMsg)) continue;

      // 主功能關鍵字（報價 / 預約 / 作品 / 風格）
      if (KEYWORD_ACTIONS[userMsg]) {
        await replyToLine(replyToken, [{ type: 'text', text: KEYWORD_ACTIONS[userMsg] }]);
        continue;
      }

      // 階層式選單（數字 / 代碼 / 觸發詞）
      const menuResult = handleMenu(userMsg);
      if (menuResult) {
        if (menuResult.type === 'flex') {
          await replyToLine(replyToken, [buildFlexMenu()]);
        } else {
          await replyToLine(replyToken, [{ type: 'text', text: menuResult.text }]);
        }
        continue;
      }

      // 關鍵字包含比對
      const answer = findAnswer(userMsg);
      const reply = answer || '感謝您的訊息！您的問題我們已收到，設計師將盡快與您聯繫。\n\n📞 市話：04-3505-2921\n📱 手機：0987-623-677\n\n輸入【常見問題】可查看常見 Q&A\n— 集思室內設計 AI 顧問';
      await replyToLine(replyToken, [{ type: 'text', text: reply }]);
    }
  } catch (err) {
    console.error('Webhook error:', err);
  }

  return res.status(200).json({ status: 'ok' });
}
